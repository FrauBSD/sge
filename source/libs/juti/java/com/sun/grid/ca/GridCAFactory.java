package com.sun.grid.ca;

/**
 * Factory class for instances of <code>GridCA</code>.
 *
 */
public class GridCAFactory {
    
    /**
     * Create a new <code>GridCA</code>
     * @param config configuration of the <code>GridCA</code>
     * @throws com.sun.grid.ca.GridCAException if th configuration is not valid
     * @return the <code>GridCA</code>
     */
    public static GridCA newInstance(final GridCAConfiguration config) throws GridCAException {
        return new GridCAImpl(config);
    }
}
